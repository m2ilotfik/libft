t_list	*lknb_lstrch(t_list *list, void *cont, size_t size)
{
	if (list == NULL)
		return (NULL);
	return (!lknb_memcmp(list->content, cont, size)) ? list :
	lknb_lstrch(list->next, cont, size);
}
