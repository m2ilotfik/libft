#include "libft.h"

void	*lknb_memccpy(void *dst, const void *src, int c, size_t n)
{
	int		i;

	i = 0;
	while (i < (int)n)
	{
		((char*)dst)[i] = ((char*)src)[i];
		i++;
		if (((char*)src)[i - 1] == c)
			return (dst + i);
	}
	return (NULL);
}
