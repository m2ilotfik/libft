#include "libft.h"

void	*lknb_memmove(void *dst, const void *src, size_t len)
{
	int		i;
	char	tmp[len + 1];

	i = 0;
	while (i < (int)len)
	{
		tmp[i] = ((char *)src)[i];
		i++;
	}
	tmp[i] = '\0';
	i = 0;
	while (i < (int)len)
	{
		((char *)dst)[i] = tmp[i];
		i++;
	}
	return (dst);
}
