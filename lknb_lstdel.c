void	lknb_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;

	while (*alst)
	{
		tmp = (*alst)->next;
		lknb_lstdelone(alst, del);
		*alst = tmp;
	}
}
