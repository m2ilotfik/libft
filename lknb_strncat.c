char	*lknb_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*tmp;

	tmp = s1;
	i = 0;
	while (*s1 != '\0')
		s1++;
	while (i < n)
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (tmp);
}
