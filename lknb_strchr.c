#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (*s == (char)c)
			return ((char*)s);
		s++;
		i--;
	}
	return (NULL);
}
