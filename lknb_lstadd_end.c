void	lknb_lstadd_end(t_list **alst, t_list *new)
{
	if (!*alst)
		*alst = new;
	else
		(*alst)->next == NULL ? (*alst)->next = new :
			lknb_lstadd_end(&(*alst)->next, new);
}
