char	*lknb_strrev(char *str)
{
	char	tmp;
	int		i;
	size_t	len;

	i = 0;
	len = lknb_strlen(str) - 1;
	while (i <= (int)len / 2)
	{
		tmp = str[i];
		str[i] = str[len - i];
		str[len - i] = tmp;
	}
	return (str);
}
