#include "libft.h"

int	lknb_memcmp(const void *s1, const void *s2, size_t n)
{
	int				i;

	i = 0;
	while (i < (int)n && ((char*)s1)[i] == ((char*)s2)[i])
	{
		i++;
	}
	if (i == (int)n)
		return (0);
	else
		return (((unsigned char*)s1)[i] - ((unsigned char*)s2)[i]);
}
